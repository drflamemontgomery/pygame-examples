from dataclasses import dataclass

def ccw(A,B,C):
    return (C.y-A.y) * (B.x-A.x) > (B.y-A.y) * (C.x-A.x)

def intersect(A, B, C, D):
    return ccw(A,C,D) != ccw(B,C,D) and ccw(A,B,C) != ccw(A,B,D)

@dataclass
class Point:
    x: float
    y: float

@dataclass
class Line:
    start: Point
    end: Point
